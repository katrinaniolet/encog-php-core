namespace Encog\ML;

/**
 * This interface defines the fact that a class, or object, is having the
 * ability to generate an Encog factory code from the objects instanciated
 * state.
 *
 */
interface MLFactory {
	/**
	 * @return string The Encog factory type code.
	 */
	public function getFactoryType();

	/**
	 * @return string The Encog architecture code.
	*/
	public function getFactoryArchitecture();
}